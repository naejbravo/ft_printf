#include "ft_printf.h"

int	ft_u_putnbr(unsigned int n)
{
	int	i;

	i = 0;
	if (n > 9)
		i += ft_u_putnbr(n / 10);
	i += ft_putchar((n % 10) + '0');
	return (i);
}
